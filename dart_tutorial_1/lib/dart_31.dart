void main() {
  int noOfDay = 5;

  //Revisa el día de la semana
  if (noOfDay == 1) {
    print("Lunes");
  } else if (noOfDay == 2) {
    print("Martes");
  } else if (noOfDay == 3) {
    print("Miércoles");
  } else if (noOfDay == 4) {
    print("Jueves");
  } else if (noOfDay == 5) {
    print("Viernes");
  } else if (noOfDay == 6) {
    print("Sábado");
  } else if (noOfDay == 7) {
    print("Domingo");
  } else {
    print("Opción Invalida");
  }
}
