void main() {
  var dayOfWeek = 5;
  switch (dayOfWeek) {
    case 1:
      print("Domingo");
      break;
    case 2:
      print("Lunes");
      break;
    case 3:
      print("Martes");
      break;
    case 4:
      print("Miércoles");
      break;
    case 5:
      print("Jueves");
      break;
    case 6:
      print("Viernes");
      break;
    case 7:
      print("Sábado");
      break;
    default:
      print("Día Invalido");
      break;
  }
}
