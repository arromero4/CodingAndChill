void main() {
  int calificacion = 8;

  switch (calificacion) {
    case 10:
      print('Excelente');
      break;

    case 9:
      print('Muy bien');
      break;

    case 8:
      print('Bien');
      break;

    case 7:
      print('Regular');
      break;

    case 6:
      print('Suficiente');
      break;

    default:
      print('Reprobaste');
      break;
  }
}
